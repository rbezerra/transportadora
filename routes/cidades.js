module.exports = app => {
	const Cidades = app.db.models.Cidades;

	app.route("/cidades")
		.get((req, res) => {
			Cidades.findAll({})
				.then(result => res.json(result))
				.catch(error => {
					res.status(412).json({msg: error.message});
				});
		})
		.post((req, res) => {
			Cidades.create(req.body)
				.then(result => res.json(result))
				.catch(error => {
					res.status(412).json({msg: error.message});
				});
		});

	app.route("/cidades/:codigo_cidade")
		.get((req, res) => {
			Cidades.findOne({where: req.params})
				.then(result => {
					if(result){
						res.json(result);
					}else{
						res.sendStatus(404);	
					}
				})
				.catch(error => {
					res.status(412).json({msg: error.message});
				});
		})
		.put((req, res) => {
			Cidades.update(req.body, {where: req.params})
				.then(result => res.sendStatus(204))
				.catch(error => {
					res.status(412).json({msg: error.message});
				});
		})
		.delete((req, res) => {
			Cidades.destroy({where: req.params})
				.then(result => res.sendStatus(204))
				.catch(error => {
					res.status(412).json({msg: error.message});
				});
		});
}